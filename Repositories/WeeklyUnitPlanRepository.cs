        #region Table Setup

        private void EnsureTableExists()
        {
            const string sql = @"
                CREATE TABLE IF NOT EXISTS WeeklyUnitPlan (
                    No INTEGER PRIMARY KEY AUTOINCREMENT,
                    YearPlanNo INTEGER NOT NULL,
                    Week INTEGER NOT NULL,
                    SectionNo INTEGER NOT NULL,
                    AllocatedHours INTEGER NOT NULL,
                    OrderInWeek INTEGER NOT NULL,
                    IsAutoGenerated INTEGER DEFAULT 1,
                    Status TEXT DEFAULT 'PLANNED',
                    FOREIGN KEY (YearPlanNo) REFERENCES SubjectYearPlan(No) ON DELETE CASCADE,
                    FOREIGN KEY (SectionNo) REFERENCES CourseSection(No) ON DELETE CASCADE
                );

                CREATE INDEX IF NOT EXISTS idx_unit_plan_week
                    ON WeeklyUnitPlan(YearPlanNo, Week);
                CREATE INDEX IF NOT EXISTS idx_unit_plan_section
                    ON WeeklyUnitPlan(YearPlanNo, SectionNo);
            ";

            using var cmd = Connection.CreateCommand();
            cmd.CommandText = sql;
            cmd.ExecuteNonQuery();

            // 기존 테이블에 UnitNo가 있으면 SectionNo로 변경 (마이그레이션)
            MigrateUnitNoToSectionNo();

            LogInfo("WeeklyUnitPlan 테이블 확인/생성 완료");
        }

        private void MigrateUnitNoToSectionNo()
        {
            try
            {
                // 컬럼 존재 여부 확인
                const string checkColumn = "SELECT EXISTS(SELECT 1 FROM pragma_table_info('WeeklyUnitPlan') WHERE name = 'UnitNo')";
                using var checkCmd = Connection.CreateCommand();
                checkCmd.CommandText = checkColumn;
                var hasUnitNo = Convert.ToInt32(checkCmd.ExecuteScalar()) == 1;

                if (hasUnitNo)
                {
                    // UnitNo 컬럼이 있으면 SectionNo로 데이터 복사 후 테이블 재생성
                    // SQLite는 ALTER COLUMN을 지원하지 않으므로 테이블 재생성 필요
                    const string migrate = @"
                        -- 임시 테이블 생성
                        CREATE TABLE IF NOT EXISTS WeeklyUnitPlan_new (
                            No INTEGER PRIMARY KEY AUTOINCREMENT,
                            YearPlanNo INTEGER NOT NULL,
                            Week INTEGER NOT NULL,
                            SectionNo INTEGER NOT NULL,
                            AllocatedHours INTEGER NOT NULL,
                            OrderInWeek INTEGER NOT NULL,
                            IsAutoGenerated INTEGER DEFAULT 1,
                            Status TEXT DEFAULT 'PLANNED'
                        );

                        -- 데이터 복사
                        INSERT OR IGNORE INTO WeeklyUnitPlan_new (No, YearPlanNo, Week, SectionNo, AllocatedHours, OrderInWeek, IsAutoGenerated, Status)
                        SELECT No, YearPlanNo, Week, UnitNo, AllocatedHours, OrderInWeek, IsAutoGenerated, Status
                        FROM WeeklyUnitPlan;

                        -- 기존 테이블 삭제
                        DROP TABLE WeeklyUnitPlan;

                        -- 새 테이블로 이름 변경
                        ALTER TABLE WeeklyUnitPlan_new RENAME TO WeeklyUnitPlan;
                    ";

                    using var migrateCmd = Connection.CreateCommand();
                    migrateCmd.CommandText = migrate;
                    migrateCmd.ExecuteNonQuery();

                    LogInfo("WeeklyUnitPlan: UnitNo → SectionNo 마이그레이션 완료");
                }
            }
            catch (Exception ex)
            {
                LogError($"WeeklyUnitPlan 마이그레이션 실패: {ex.Message}", ex);
            }
        }

