        /// <summary>
        /// 단원 배치 생성
        /// </summary>
        public async Task<int> CreateAsync(WeeklyUnitPlan plan)
        {
            const string query = @"
                INSERT INTO WeeklyUnitPlan (
                    YearPlanNo, Week, SectionNo, AllocatedHours,
                    OrderInWeek, IsAutoGenerated, Status
                ) VALUES (
                    @YearPlanNo, @Week, @SectionNo, @AllocatedHours,
                    @OrderInWeek, @IsAutoGenerated, @Status
                );
                SELECT last_insert_rowid();";

            try
            {
                using var cmd = CreateCommand(query);
                AddParameters(cmd, plan);

                var result = await cmd.ExecuteScalarAsync();
                plan.No = Convert.ToInt32(result);

                LogInfo($"단원 배치 생성 완료: No={plan.No}, Week={plan.Week}, SectionNo={plan.SectionNo}");
                return plan.No;
            }
            catch (Exception ex)
            {
                LogError($"단원 배치 생성 실패: Week={plan.Week}, SectionNo={plan.SectionNo}", ex);
                throw;
            }
        }

        /// <summary>
        /// 단원 배치 일괄 생성
        /// </summary>
        public async Task<int> CreateBatchAsync(List<WeeklyUnitPlan> plans)
        {
            if (plans == null || plans.Count == 0)
                return 0;

            int count = 0;

            try
            {
                BeginTransaction();

                foreach (var plan in plans)
                {
                    await CreateAsync(plan);
                    count++;
                }

                Commit();
                LogInfo($"단원 배치 일괄 생성 완료: {count}개");
                return count;
            }
            catch (Exception ex)
            {
                Rollback();
                LogError($"단원 배치 일괄 생성 실패", ex);
                throw;
            }
        }

