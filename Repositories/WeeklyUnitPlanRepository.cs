        #region Helper Methods

        private void AddParameters(SqliteCommand cmd, WeeklyUnitPlan plan)
        {
            cmd.Parameters.AddWithValue("@No", plan.No);
            cmd.Parameters.AddWithValue("@YearPlanNo", plan.YearPlanNo);
            cmd.Parameters.AddWithValue("@Week", plan.Week);
            cmd.Parameters.AddWithValue("@SectionNo", plan.SectionNo);
            cmd.Parameters.AddWithValue("@AllocatedHours", plan.AllocatedHours);
            cmd.Parameters.AddWithValue("@OrderInWeek", plan.OrderInWeek);
            cmd.Parameters.AddWithValue("@IsAutoGenerated", plan.IsAutoGenerated);
            cmd.Parameters.AddWithValue("@Status", plan.Status ?? "PLANNED");
        }

        private WeeklyUnitPlan MapPlan(SqliteDataReader reader)
        {
            return new WeeklyUnitPlan
            {
                No = reader.GetInt32(reader.GetOrdinal("No")),
                YearPlanNo = reader.GetInt32(reader.GetOrdinal("YearPlanNo")),
                Week = reader.GetInt32(reader.GetOrdinal("Week")),
                SectionNo = reader.GetInt32(reader.GetOrdinal("SectionNo")),
                AllocatedHours = reader.GetInt32(reader.GetOrdinal("AllocatedHours")),
                OrderInWeek = reader.GetInt32(reader.GetOrdinal("OrderInWeek")),
                IsAutoGenerated = reader.GetInt32(reader.GetOrdinal("IsAutoGenerated")),
                Status = reader.GetString(reader.GetOrdinal("Status"))
